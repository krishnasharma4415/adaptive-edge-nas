# 1_teacher_training.py – Train ResNet‑50 teacher on cat‑dog spectrograms

"""
This script trains a ResNet‑50 (ImageNet‑pretrained) as the teacher model for the
cat‑dog voice classification task. It uses the processed 3‑channel 64×64 log‑Mel
spectrograms generated by `0_data_processing.py`.

Key features (as requested by the user):
  • No `def main()` – the script runs on import.
  • Minimal helper functions – only those that provide a clear benefit.
  • Heavy GPU utilisation: AMP, channels_last memory format, torch.compile,
    non‑blocking data transfers, and a cosine LR scheduler.
  • Early‑stopping based on validation accuracy.
"""

import pathlib
import pickle
import random
import warnings
from collections import defaultdict

import torch
import torch.nn as nn
import torch.optim as optim
from torch.utils.data import Dataset, DataLoader
import torchvision.models as models

warnings.filterwarnings("ignore")

# ── Reproducibility ───────────────────────────────────────────────────────────────
SEED = 42
random.seed(SEED)
torch.manual_seed(SEED)
if torch.cuda.is_available():
    torch.cuda.manual_seed_all(SEED)
    torch.backends.cudnn.benchmark = True
    torch.backends.cudnn.deterministic = False
DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print(f"Device: {DEVICE}")

# ── Paths ───────────────────────────────────────────────────────────────────────
BASE_DIR = pathlib.Path(__file__).resolve().parents[3]  # project root
MANIFEST_PATH = BASE_DIR / "experiments" / "cat-dog_voice" / "processed" / "data_manifest.pkl"
MODEL_SAVE_PATH = BASE_DIR / "models" / "cat_dog_teacher.pth"
MODEL_SAVE_PATH.parent.mkdir(parents=True, exist_ok=True)

# ── Load manifest ────────────────────────────────────────────────────────────────
with open(MANIFEST_PATH, "rb") as f:
    manifest = pickle.load(f)

# ── Simple Dataset loading the pre‑saved torch tensors ─────────────────────────────
class SpectrogramDataset(Dataset):
    def __init__(self, entries):
        self.entries = entries  # list of (tensor_path, label)

    def __len__(self):
        return len(self.entries)

    def __getitem__(self, idx):
        path, label = self.entries[idx]
        tensor = torch.load(path, map_location=DEVICE)  # (3,64,64)
        return tensor.to(DEVICE, non_blocking=True), torch.tensor(label, dtype=torch.long, device=DEVICE)

train_dataset = SpectrogramDataset(manifest["train"])
val_dataset = SpectrogramDataset(manifest["val"])

BATCH_SIZE = 64
NUM_WORKERS = 4
train_loader = DataLoader(
    train_dataset,
    batch_size=BATCH_SIZE,
    shuffle=True,
    num_workers=NUM_WORKERS,
    pin_memory=True,
    persistent_workers=True,
)
val_loader = DataLoader(
    val_dataset,
    batch_size=BATCH_SIZE,
    shuffle=False,
    num_workers=NUM_WORKERS,
    pin_memory=True,
    persistent_workers=True,
)

# ── Model – ResNet‑50 pretrained, replace final FC for 2 classes ───────────────────
model = models.resnet50(pretrained=True)
# Preserve the original conv1 (expects 3‑channel input – our spectrograms are 3‑channel)
model.fc = nn.Linear(model.fc.in_features, 2)
model = model.to(DEVICE)
model = model.to(memory_format=torch.channels_last)  # channels‑last layout
model = torch.compile(model)  # JIT‑style compilation for speed

criterion = nn.CrossEntropyLoss()
optimizer = optim.AdamW(model.parameters(), lr=1e-4, weight_decay=1e-4)
# Cosine LR scheduler with warm‑up (first 5 epochs)
scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=40)

# AMP scaler
scaler = torch.cuda.amp.GradScaler()

# ── Training utilities ─────────────────────────────────────────────────────────────
def evaluate(loader):
    model.eval()
    correct = 0
    total = 0
    with torch.no_grad():
        for inputs, targets in loader:
            outputs = model(inputs)
            _, preds = torch.max(outputs, 1)
            correct += (preds == targets).sum().item()
            total += targets.size(0)
    return correct / total if total > 0 else 0.0

best_val_acc = 0.0
patience = 10
epochs_no_improve = 0
MAX_EPOCHS = 50

for epoch in range(1, MAX_EPOCHS + 1):
    model.train()
    epoch_loss = 0.0
    for inputs, targets in train_loader:
        optimizer.zero_grad(set_to_none=True)
        with torch.cuda.amp.autocast():
            outputs = model(inputs)
            loss = criterion(outputs, targets)
        scaler.scale(loss).backward()
        scaler.step(optimizer)
        scaler.update()
        epoch_loss += loss.item() * inputs.size(0)
    scheduler.step()

    val_acc = evaluate(val_loader)
    print(f"Epoch {epoch:02d} – Train loss: {epoch_loss/len(train_loader.dataset):.4f} – Val Acc: {val_acc*100:.2f}%")

    # Early stopping check
    if val_acc > best_val_acc:
        best_val_acc = val_acc
        epochs_no_improve = 0
        torch.save(model.state_dict(), MODEL_SAVE_PATH)
        print(f"✅ New best model saved (val acc {val_acc*100:.2f}%)")
    else:
        epochs_no_improve += 1
        if epochs_no_improve >= patience:
            print("⏹ Early stopping triggered.")
            break

print(f"Training finished – best validation accuracy: {best_val_acc*100:.2f}%")

# End of script – runs automatically when the file is executed/imported.
